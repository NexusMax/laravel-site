     */
    public function index()
    {

        $data = [];
        $data['orders_count'] = Orders::where('dt', '>', DATE("Y-m-d H:i:s"))->count();
        $data['items_count'] = Items::where('published', 1)->count();
        $data['categories_count'] = Categories::where('published', 1)->count();
        $data['events'] = ItemsEvents::where('end_at', '>', DATE("Y-m-d H:i:s"))->orderBy('end_at', 'desc')->get();

        return view('admin.index')
            ->with('name', 'Доска')
            ->with('data', $data);
    }

